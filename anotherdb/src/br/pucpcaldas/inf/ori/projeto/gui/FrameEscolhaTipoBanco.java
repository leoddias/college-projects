    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (Session.canCreate) {
            String nomeBanco = "";
            do {
                nomeBanco = JOptionPane.showInputDialog("Digite o nome do banco:");
                if (nomeBanco.isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Digite um nome para o banco!");
                }
            } while (nomeBanco.isEmpty());

            try {
                SQL query = new SQL();
                query.ExecutaSQL("CREATE DATABASE " + nomeBanco);
            } catch (InterruptedException ex) {
                JOptionPane.showMessageDialog(rootPane, "erro ao criar um banco!");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "erro ao criar um banco!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Você não tem permissão para criar um banco!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

